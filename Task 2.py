a = float(input("Enter a value: "))
b = float(input("Enter b value: "))
op = input("Enter the operator:")
if op == "+" :
    print(a + b)
elif op == "-" :
    print(a - b)
elif op == "-" :
    print(a - b)
elif op == "*" :
    print(a * b)
elif op == "/" :
    print(a / b)
elif op == "//" :
    print(a // b)
elif op == "%" :
    print(a % b)
elif op == "**" :
    print(a ** b)
else:
    print("Operation is Invalid")    